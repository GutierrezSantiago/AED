#String o cadena de caracteres
print("Hola mundo")
#Caracteres especiales
#Caracteres de escape o de control
print("Hola\nmundo")
print("Hola\n\nmundo")
#Caracter de tabulación
print("Hola\tmundo")
#Caracter barra invertida
print("Hola\\mundo")
#Quiero que la comilla quede dentro del string
print('Quiero ir a Mc\'Donalds')
#Tambien se pueden utilizar distintas las comillas
print("Quiero ir a Mc'Donalds")
print('Quiero ir a Mc"Donalds')
print("\"Hola\"")

s = "Hola"
m = "mundo"
suma = s + " " + m
print(suma)
#String se puede multiplicar para usos como el siguiente
s = "-----------------"
h = "-" * 20
print(s)
print(h)
#Asignación de tupla en Python
a = 5
#Operacion de empaquetado y desempaquetado simultanea
x, y = 2*a, 4*a
print(x)
print(y)
