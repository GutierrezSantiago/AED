#Programa para calcular el perimetro
print("Programa para calcular el perimetro de un triangulo")

#Ingreso de datos
lad1 = float(input("Ingrese el lado 1: "))
lad2 = float(input("Ingrese el lado 2: "))
lad3 = float(input("Ingrese el lado 3: "))

#Proceso
p = lad1 + lad2 + lad3

#Resultado
print("El perimetro del triangulo es", p)
